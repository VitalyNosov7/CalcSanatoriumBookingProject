
namespace CalcSanatoriumBooking.Resources
{
    /// <summary>   Знаки сравнения: =, !=, >, <, >=, >= </summary>
	public enum ComparisonSign
    {
		EqualSign,			//	Знак равно: =
		NotEqualSign,		//	Знак не равно: !=
		MoreSign,			//	Знак больше: >
		LessSign,			//	Знак меньше: <
		MoreOrEqualSign,    //	Знак больше или равно: >=
		LessOrEqualSign,    //	Знак меньше или равно: <=
	}
}
